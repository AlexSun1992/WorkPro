<script>
import { BNav, BNavItem } from "bootstrap-vue";
import LoginForm from "../LoginForm.vue";
import RegForm from "../RegForm/RegForm.vue";

export default {
  name: "AuthForm",
  components: {
    LoginForm,
    RegForm,
    BNav,
    BNavItem,
  },
  data() {
    return {
      visibleForm: null,
    };
  },
  methods: {
    redirectWithRef(path) {
      const url = new URL(path, window.location.origin);
      const DEFAULT_SUCCESS_REF = "/cabinet";
      const currentUrl = new URL(window.location.href);
      url.searchParams.set(
        "ref",
        currentUrl.searchParams.get("ref") || DEFAULT_SUCCESS_REF
      );
      window.location.href = url.href;
    },
    toggleForm(address) {
      if (address === "registration") {
        window.location.href = "/login/registration";
      }
      if (address === "login") {
        window.location.href = "/login";
      }
    },
    goESIA() {
      this.$LogEvent({
        formName: "AuthForm",
        idEventType: 18,
        controlName: "AuthForm.vue",
        message: `Нажал на кнопку «Войти через ГОСУСЛУГИ»`,
        timeUser: new Date(),
      });
      this.redirectWithRef("/sso?auth&type=esia");
    },
    goALFA() {
      this.$LogEvent({
        formName: "AuthForm",
        idEventType: 503,
        controlName: "AuthForm.vue",
        message: `Нажал на кнопку «Войти через AlfaID`,
        timeUser: new Date(),
      });
      this.redirectWithRef("/sso?auth&type=alfa");
    },
    goMobileID() {
      this.$LogEvent({
        formName: "AuthForm",
        idEventType: 521,
        controlName: "AuthForm.vue",
        message: `Нажал на кнопку «Войти через MobileID`,
        timeUser: new Date(),
      });
      this.redirectWithRef("/sso?auth&type=mobileid");
    },
  },
  mounted() {
    const currentURL = window.location.pathname;
    this.visibleForm = currentURL.includes("registration")
      ? "registration"
      : "login";
  },
};
</script>

